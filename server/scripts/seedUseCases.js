          problem_en = EXCLUDED.problem_en,
          solution_tr = EXCLUDED.solution_tr,
          solution_en = EXCLUDED.solution_en,
          seo_title_tr = EXCLUDED.seo_title_tr,
          seo_title_en = EXCLUDED.seo_title_en,
